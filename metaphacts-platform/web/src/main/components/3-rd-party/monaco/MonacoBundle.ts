export * from 'monaco-editor/esm/vs/editor/editor.api.js';

import { PageService } from 'platform/api/services/page';

(window as any).MonacoEnvironment = {
  getWorkerUrl(moduleId: string, label: string) {
    const bundleName = getMonacoWorkerBundleName(label);
    return PageService.getWorkerBundleUrl(bundleName);
  }
};

function getMonacoWorkerBundleName(label: string) {
  // see "platform-web-build.json" for worker entry names
  switch (label) {
    case 'json':
      return 'monaco.json.worker';
    case 'css':
      return 'monaco.css.worker';
    case 'html':
      return 'monaco.html.worker';
    case 'mp-template':
      return 'monaco.mp-template.worker';
    default:
      return 'monaco.editor.worker';
  }
}
